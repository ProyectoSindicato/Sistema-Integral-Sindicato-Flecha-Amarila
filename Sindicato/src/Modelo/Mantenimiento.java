package Modelo;
public class Mantenimiento {
    private String claveConductor;
    private String claveAutobus;
    private String nombreConductor;
    private String fecha;
    private String motivo;
    private String idEmpleado;
    private String claveMantenimiento;
    private String nombreJefe;

    public String getClaveConductor() {
        return claveConductor;
    }

    public void setClaveConductor(String claveConductor) {
        this.claveConductor = claveConductor;
    }

    public String getClaveAutobus() {
        return claveAutobus;
    }

    public void setClaveAutobus(String claveAutobus) {
        this.claveAutobus = claveAutobus;
    }

    public String getNombreConductor() {
        return nombreConductor;
    }

    public void setNombreConductor(String nombreConductor) {
        this.nombreConductor = nombreConductor;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getMotivo() {
        return motivo;
    }

    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }

    public String getIdEmpleado() {
        return idEmpleado;
    }

    public void setIdEmpleado(String idEmpleado) {
        this.idEmpleado = idEmpleado;
    }

    public String getClaveMantenimiento() {
        return claveMantenimiento;
    }

    public void setClaveMantenimiento(String claveMantenimiento) {
        this.claveMantenimiento = claveMantenimiento;
    }

    public String getNombreJefe() {
        return nombreJefe;
    }

    public void setNombreJefe(String nombreJefe) {
        this.nombreJefe = nombreJefe;
    }

}

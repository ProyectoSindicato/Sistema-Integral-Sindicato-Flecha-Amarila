package Modelo;
public class AsesoriaLegal {
    
    private String claveConductor;
    private String nombreConductor;
    private String fecha;
    private String tipo;
    private String idEmpleado;
    private String claveAsesoria;
    private String nombreJefe;
    private String observaciones;

    public String getClaveConductor() {
        return claveConductor;
    }

    public void setClaveConductor(String claveConductor) {
        this.claveConductor = claveConductor;
    }

    public String getNombreConductor() {
        return nombreConductor;
    }

    public void setNombreConductor(String nombreConductor) {
        this.nombreConductor = nombreConductor;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getIdEmpleado() {
        return idEmpleado;
    }

    public void setIdEmpleado(String idEmpleado) {
        this.idEmpleado = idEmpleado;
    }

    public String getClaveAsesoria() {
        return claveAsesoria;
    }

    public void setClaveAsesoria(String claveAsesoria) {
        this.claveAsesoria = claveAsesoria;
    }

    public String getNombreJefe() {
        return nombreJefe;
    }

    public void setNombreJefe(String nombreJefe) {
        this.nombreJefe = nombreJefe;
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }
    
}
